/**
 * inject-scheduled-handler.mjs
 *
 * Post-build script: appends a `scheduled` event handler to the
 * opennextjs/cloudflare-generated worker.js so Cloudflare Cron Triggers
 * can warm the Worker's V8 isolate every 15 minutes.
 *
 * Why injection and not a custom entrypoint?
 *   opennextjs/cloudflare 1.x generates .open-next/worker.js at build time.
 *   There is no supported hook to extend it at source level — the build
 *   always overwrites the file. Post-build injection is the only safe path
 *   that survives opennext upgrades: the regex targets the stable closing
 *   pattern of the default export, not internal implementation details.
 *
 * Usage (already wired into npm run deploy via package.json):
 *   node scripts/inject-scheduled-handler.mjs
 */

import { readFileSync, writeFileSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const WORKER_PATH = resolve(__dirname, '../.open-next/worker.js');

// Money pages to warm — covers Home, two top product pages, Pricing, and the
// three vertical landing pages. Italian locale is the primary traffic locale.
// Adjust if analytics show another locale dominating cold-start latency.
const WARM_PAGES = [
  '/',
  '/it/',
  '/it/products/geotapp-flow/',
  '/it/products/geotapp-app/',
  '/it/pricing/',
  '/it/settori/',
  '/it/settori/pulizie/',
  '/it/settori/installatori/',
  '/it/settori/sicurezza/',
];

const SCHEDULED_HANDLER = `
// ─── Scheduled pre-warming handler (injected by scripts/inject-scheduled-handler.mjs) ──
// Fires every 15 minutes via the Cloudflare Cron Trigger configured in wrangler.jsonc.
// Warming these URLs keeps the V8 isolate alive and the Cloudflare edge cache hot,
// which is responsible for roughly 60-80% of observed cold-start TTFB spikes.
const __WARM_PAGES__ = ${JSON.stringify(WARM_PAGES)};

async function __warmWorker__(origin) {
  await Promise.allSettled(
    __WARM_PAGES__.map((path) =>
      fetch(\`\${origin}\${path}\`, {
        method: 'GET',
        headers: { 'User-Agent': 'GeoTapp-Warmer/1.0', 'x-warmup': '1' },
        signal: AbortSignal.timeout(10000),
      })
    )
  );
}
`;

// The injection target: the closing brace of the default export object.
// Matches the exact pattern generated by opennextjs/cloudflare 1.x.
// If the pattern changes across opennext versions, the script exits non-zero
// and the deploy is blocked — a deliberate fail-fast safeguard.
const EXPORT_DEFAULT_CLOSE = /^(export default \{[\s\S]*?)(^\};)$/m;

let worker;
try {
  worker = readFileSync(WORKER_PATH, 'utf-8');
} catch {
  console.error(`❌  Worker not found at ${WORKER_PATH} — run opennextjs build first.`);
  process.exit(1);
}

if (worker.includes('__warmWorker__')) {
  console.log('ℹ️  Scheduled handler already injected — skipping.');
  process.exit(0);
}

// The fetch handler already ends with a trailing comma ("    },").
// We only append the new method — no leading comma needed.
const patched = worker.replace(
  EXPORT_DEFAULT_CLOSE,
  (_, body, closing) =>
    `${SCHEDULED_HANDLER}${body}  async scheduled(controller, env, ctx) {
    const origin = 'https://geotapp.com';
    ctx.waitUntil(__warmWorker__(origin));
  }
${closing}`,
);

if (patched === worker) {
  console.error(
    '❌  Injection failed — export default closing pattern not found.\n' +
    '    Check that .open-next/worker.js ends with the expected "export default { ... };" block.',
  );
  process.exit(1);
}

writeFileSync(WORKER_PATH, patched, 'utf-8');
console.log('✅  Scheduled pre-warming handler injected into .open-next/worker.js');
